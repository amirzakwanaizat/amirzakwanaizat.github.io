                            </p>
                        <strong>Why do you need Phishing Simulation?</strong>
                            <ul class="flat-list style1">
                                <li>
                                A phishing simulation is the best way to test an organization’s human capital susceptibility to a social engineering attack by putting them in a real life situation and capturing their response towards it
                                </li>
                                <li>
                                It helps to identify employees who are vulnerable to phishing attacks. Additional training is then conducted for the evaluated personnel
                                </li>
                                <li>
                                It helps an organization in tracking response from previous campaigns and analyzing the data/ response trend
                                </li>
