                        <h3 id="anch6" class="flat-title-section style"><span>Data</span> Governance</h3>
                        <p>
                        Data Governance ensures high data quality exists throughout its lifecycle within the organization. The key focus areas of Data Governance include confidentiality, integrity, availability, accessibility and nonrepudiation of data owned and managed by the organization. All aspects including people, process and technology need to be addressed while implementing a comprehensive Data Governance Framework.
                        </p>
                        <strong>Why do you need Data Governance?</strong>
                        <p>
                        Data is the lifeline of almost all businesses in today’s world. Data that is not governed or managed appropriately can cause significant losses to the organizations. These include:
                        </p>
                        <ul class="flat-list style1">
                            <li>
                            Loss/leakage of data: Data Governance helps in the identification, classification and appropriate handling of data within the organization. When these are not implemented effectively, it is difficult to track and protect data.
                            </li>
                            <li>
                            Regulatory non-compliance: It helps to prepare any organization to comply with applicable regulatory compliances such as GDPR (General Data Protection Regulation).
                            </li>
                            <li>
                            Ineffective use of data: It is not possible to harness the power of data through processing or analytics if it’s not governed properly. It is evident how some organizations have progressed by using these techniques.
                            </li>
                        </ul>
                        

