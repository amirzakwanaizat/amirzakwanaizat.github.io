                        <h3 id="anch4" class="flat-title-section style"><span>Business Continuity Planning</span> and <span>Disaster Recovery Strategy</span></h3>
                        <p>
                        In today’s world, information and services are required 24/7, as any disruption to operations may affect business transactions and result in a direct or indirect loss of profits. Business Continuity Planning and Disaster Recovery Strategy and Roadmap helps the organizations to respond quickly to ensure critical business functions continue without interruption.
                        </p>
                        <strong>Why do you need Business Continuity?</strong>
                        <p>
                        Business Continuity Planning helps the organization to deal with risks and ensure continuous business operation over a short period of time, phase by phase, beginning from the most critical business functions to less essential functions.
                        </p>
                        <p>
                        Below are the three elements of Business Continuity Planning:
                        </p>
                        <ul class="flat-list style1">
                            <li>
                            High Availability
                            </li>
                            <li>
                            Continuous Operation
                            </li>
                            <li>
                            Disaster Recovery
                            </li>
                        </ul>
                        <strong>Why do you need Disaster Recovery Strategy?</strong>
                        <p>
                        Disaster Recovery Strategy and Roadmap details action plans that will be carried out before, during and after an IT disruption which causes the system to be unavailable for a period of time.
                        </p>
                        <p>
                        The principal objectives are:
                        </p>
                        <ul class="flat-list style1">
                            <li>
                            Respond to critical disruptions to business operations
                            </li>
                            <li>
                            Minimize the duration of interruption to services/resources
                            </li>
                            <li>
                            Minimize immediate damages and losses
                            </li>
                            <li>
                            Facilitate effective coordination of recovery tasks
                            </li>
                        </ul>
                        

