                                        <div class="toggle-content">
                                        <strong>
                                                Background
                                            </strong><br>
                                            <p>
                                            A leading business group in India reached to assess the susceptibility of their senior staff towards phishing attacks. “Enticing our employees to enter their login credentials or click on links was simply too run-of-the-mill,” one of them told us.
                                            </p>
                                            <p>
                                            They wanted to gauge how susceptible their employees were to phishing attacks, but they did not want something quite so simple. 
                                            </p>

                                            <strong>
                                                What We Did
                                            </strong><br>
                                            <p>
                                            We found out that an internal conference involving the senior leadership was being organized at an external venue. We crafted a phishing email that appeared to have come from their HR and asked them to dress in red tops and black pants during the conference. The results couldn’t be any clearer. Anyone who fell for the simulated phishing attack would dress as the email told them to. 
                                            </p>
                                            <p>
                                            The executives were finally onboard.
                                            </p>

                                            <strong>
                                                Outcome
                                            </strong><br>
                                            <p>
                                            Out of 40 participants, 20 took the bait and came in red tops and black pants. Management was convinced that there was a need to improve their employees’ security awareness and subsequently engaged OhPhish’s services.  
                                            </p>
                                        </div><!-- /.toggle-content -->
                                    </div><!-- /.flat-toggle -->

                                    <div class="flat-toggle">
                                        <h6 class="toggle-title">Case Study 2</h6>
                                        <div class="toggle-content">
                                        <strong>
                                                Background
                                            </strong><br>
                                            <p>
                                            One of the biggest stock exchanges of the world approached us and explained that while they already had good security measures in place to protect their business, they realized that their employees may still susceptible to the ever-evolving cybersecurity threats. They had also seen several incidents involving human error and they knew that something had to be done to engage their employees.
                                            </p>

                                            <strong>
                                                What We Did
                                            </strong><br>
                                            <p>
                                            Our team set up a fake reward redemption website and sent their employees email regarding winning of long service awards. The employees clicked the given link to go to the fake website and choose / redeem the rewards. A message stating that that they had successfully redeemed their rewards were sent to them at the end.
                                            </p>

                                            <strong>
                                                Outcome
                                            </strong><br>
                                            <p>
                                            As the simulation was done without prior notice, most of the employees clicked and fell for the scam. The exercise went undisclosed for one month and during that period, many continued to click on the link to check and monitor the status of their reward redemption. This simulation was a huge success with more than 90% of users falling for it.
                                            </p>
