                                                <br>
                                                <p><strong>THE UBER BREACH</strong></p>
                                                <p>On Tuesday, 27th of November 2018, The United Kingdom’s Information Commissioner’s Office said that it has fined Uber £385,000 for failing to protect their customers’ data. The breach, which took place in 2016, had affected millions of users.</p>
                                                <p>The 2016 cyberattack allowed hackers to steal a significant amount of personal information such as names, addresses and phone numbers of Uber’s customers, as well as names and number plates of Uber drivers from a cloud storage operated by Uber’s parent company in the United States.</p>
                                                <p>The Dutch Data Protection Authority has also fined Uber £600,000 for violating the Dutch data breach regulation and for failing to report the data breach to the Dutch DPA within 72 hours after the discovery of the breach.</p>
                                                <p><strong>WHAT ACTUALLY HAPPENED?&nbsp;</strong></p>
                                                <p>A data breach occurs when a cybercriminal extracts sensitive information by infiltrating a data source. Cybercriminals can do this by bypassing a networking security remotely, physically accessing a computer or through phishing attacks.</p>
                                                <p>It only takes one weak link for a breach to happen, and this is what probably happened at Uber as well. Uber said that two people who didn’t work for the company accessed a GitHub coding site used by Uber software engineers to store code. It is on this site that hackers found a set of login credentials and used those credentials to access a third-party cloud-based service holding all the sensitive user data of Uber drivers and customers.</p>
                                                <blockquote><p><strong>HOW CAN WE PREVENT THIS?</strong></p>
                                                    <ul class="flat-list style1">
                                                        <li>Train your (IT as well as business) personnel against phishing attacks. Doing periodic phishing simulations helps in training them on identifying and defending against such attacks.</li>
                                                        <li>Implementing a multifactor authentication for accessing sensitive information like PII and source codes can prevent hackers from gaining access.</li>
                                                        <li>As most breaches often involve a large volume of personal information stored in a central server, segmenting the data more judiciously instead of having such large amounts of sensitive data stored in a single account would not create an irresistible target for hackers.</li>
                                                        <li>Incident response processes must be improved to effectively respond to a cyberattack.</li>
                                                        <li>Regular security checks must be performed, especially on critical information infrastructure systems</li>
                                                    </ul>
                                                </blockquote>
                                                <p><strong>HERE ARE OTHER THINGS THAT YOU NEED TO KNOW...</strong></p>
                                                <p>For Uber drivers, the company says it doesn’t believe that affected individuals need to take any action. “We have seen no evidence of fraud or misuse tied to the incident,” in a statement to Uber’s drivers. “We are monitoring the affected accounts and have flagged them for additional protection.”</p>
                                                <p>However, when a data breach of this magnitude is reported all over the news, scammers may just take advantage of the situation. EGS recommends users to take the following steps:</p>
                                                    <ul class="flat-list style1">
                                                        <li>Be on the lookout for phishing scams. If you get an email requesting for your personal information from Uber, delete the email immediately. It is probably a phishing attempt to get your personal information.</li>
                                                        <li>Monitor your accounts for suspicious activity.</li>
                                                        <li>If you get an SMS stating that it’s from Uber, get it verified before clicking on any link accompanying the message.</li>
                                                    </ul>
