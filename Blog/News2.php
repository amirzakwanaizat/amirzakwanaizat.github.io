                                                <p><strong>FOUR STEPS TO STAY SAFE AFTER FACEBOOK DATA BREACH</strong></p>
                                                <p>We all heard about the Facebook data breach that took place in September 2018. 30 million user accounts were impacted. Out of the 30 million affected users, 14 million had their names, contact details and other information such as gender, relationship status and location check-ins exposed in the breach.</p>
                                                <p>According to a Facebook blog post, the attackers were said to have exploited a vulnerability in Facebook’s code that existed between July 2017 and September 2018. The vulnerability was the result of a complex interaction of three software bugs and it impacted the “View As” feature, which allows users to see what their profile will look like to others.</p>
                                                <p>This vulnerability allowed hackers to use 400,000 accounts under their control to gain the access tokens of 30 million Facebook users. Access tokens are basically digital keys which keep people logged in on Facebook without having them to re-enter their password every time they use their app.</p>
                                                <p>The cyber threat landscape is constantly evolving. However, this doesn’t mean that we can’t be ready for it. EGS recommends users to take the following steps to stay vigilant and keep safe from future attacks:</p>
                                                    <ul class="flat-list style1">
                                                        <li>Enable two-factor authentication. This way even if someone obtains your account, they can’t login your account without the code.</li>
                                                        <li>Be alert to phishing attacks. Educate yourself on the latest phishing scams.</li>
                                                        <li>Ensure that your password is secure. Use unique and difficult-to-guess passwords.</li>
                                                        <li>In one of the blog posts by Facebook, it says that users can visit the “Security and Login” tab in the setting menu. Check to see the list of services that you’re signed in with, and sign out for services which you no longer want to use through your login.</li>
